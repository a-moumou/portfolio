<p class="veille-def">
    La veille technologique ou veille scientifique et technique consiste à s’informer de façon systématique sur
    les techniques les plus récentes et surtout sur leur mise à disposition commerciale (ce qui constitue la différence
    entre la technique et la technologie). Cette activité met en œuvre des techniques d’acquisition,
    de stockage et d’analyse d’informations et leur distribution automatique aux différentes sections concernées de l’entreprise,
    à la manière d’une revue de presse. Ces informations peuvent concerner une matière première, un produit, un composant,
    un procédé, l’état de l’art et l’évolution de l’environnement scientifique, technique, industriel ou commercial de l’entreprise.
